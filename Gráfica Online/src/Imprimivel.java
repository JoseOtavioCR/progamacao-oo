/**
 * A interface {@code Imprimivel} define um contrato para classes que possuem
 * a capacidade de imprimir suas informações.
 *
 * Qualquer classe que implemente esta interface deve fornecer uma implementação
 * do método {@code imprimir()}.
 *
 * @author José Otávio
 */
public interface Imprimivel {

   /**
    * Imprime as informações da classe que implementa esta interface.
    */
   void imprimir();
}

